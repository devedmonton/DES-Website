                    proxy. Every question submitted to any meeting of members
                    shall be decided on a show of hands except when a ballot is
                    required by the chair of the meeting or is demanded by not
                    less than five (5) members entitled to vote at the meeting.
                    A member may demand a ballot either before or on the
                    declaration of the result of any vote by show of hands.
                </p>
                <p class="py-2">
                    Except for the special resolutions of the Society, each
                    question and resolution shall be decided by a majority of
                    the votes cast by the members present or represented by a
                    proxy. At every meeting at which they are entitled to vote,
                    every member present in person or represented by proxy shall
                    have one (1) vote. In the case of an equality of votes the
                    chair of the meeting shall either on a show of hands or on a
                    ballot, have a second or casting vote in addition to the
                    vote to which they may be entitled as a member.
                </p>
