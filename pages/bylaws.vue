            <h2 class="text-xl font-bold py-2">Board Of Directors</h2>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                5.01 General
            </h2>
            <p class="py-2">
                The business and affairs of the Society shall be managed by the
                directors of the Society who may exercise all such powers and do
                all such acts and things as may be exercised or done by the
                Society and are not by the Act, the Bylaws, any special
                resolution of the Society, or by statute expressly directed or
                required to be done in some other manner.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                5.02 Number
            </h2>
            <p class="py-2">
                The number of directors shall be not less than five (5) persons
                and not more than nine (9) persons and shall be determined from
                time to time within such limits by resolution of the Board.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                5.03 Vacancies
            </h2>
            <p class="py-2">
                A quorum of directors may fill a vacancy among the directors,
                except a vacancy resulting from an increase in the number or
                minimum number of directors or from a failure to elect the
                minimum number of directors. If there is not a quorum of
                directors, or if there has been a failure to elect the minimum
                number of directors, the directors then in office shall
                forthwith call a special meeting of members to fill the vacancy
                and, if they fail to call a meeting or if there are no directors
                then in office, the meeting may be called by any member. If the
                members have adopted an amendment to the articles to increase
                the minimum number of directors, and have not, at the meeting at
                which they adopted the amendment, elected an additional number
                of directors authorized by the amendment, the directors then in
                office shall forthwith call a special meeting of members to fill
                the vacancy.
            </p>
            <p class="py-2">
                A director appointed or elected to fill a vacancy holds office
                for the unexpired term of their predecessor.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                5.04 Duties
            </h2>
            <p class="py-2">
                Every director and officer of the Society in exercising their
                powers and discharging their duties shall:
            </p>
            <ol class="ml-10" type="a">
                <li class="py-2">
                    (a) act honestly and in good faith with a view to the best
                    interests of the Society; and
                </li>
                <li class="py-2">
                    (b) exercise the care, diligence and skill that a reasonably
                    prudent person would exercise in comparable circumstances
                </li>
            </ol>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                5.05 Term Of Office
            </h2>
            <p class="py-2">
                Directors may hold office for terms of three years, which shall
                be from the date of the meeting at which that director is
                elected until the close of the third annual meeting following
                their election or until that director's successor is elected or
                appointed. If a director is not elected at a meeting of members,
                the incumbent directors continue in office until their
                successors are elected.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                5.06 Election
            </h2>
            <p class="py-2">
                At each annual/general meeting of the members of the Society,
                not less than five (5) and not more than nine (9) directors
                shall be appointed by a vote of the members of the Society.
                Notwithstanding the foregoing, the directors appointed to the
                Board shall be selected so that at no time shall more than 50%
                of the directors be arms-length directors as such term is
                defined in the Income Tax Act (Canada)
            </p>
            <p class="py-2">
                If a meeting of members fails to elect the number or the minimum
                number of directors required by the Bylaws by reason of the
                disqualification or death of any candidate, the directors
                elected at that meeting may exercise all the powers of the
                directors if the number of directors so elected constitutes a
                quorum.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                5.07 Consent To Election
            </h2>
            <p class="py-2">
                A person who is elected or appointed a director is not a
                director unless they were present at the meeting when they were
                elected or appointed and did not refuse to act as a director or,
                if they were not present at the meeting when they were elected
                or appointed, they consented to act as a director in writing
                before their election or appointment or within ten (10) days
                after it or they have acted as a director pursuant to the
                election or appointment.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                5.08 Removal
            </h2>
            <p class="py-2">
                The members of the Society may by ordinary resolution at a
                special meeting called for that purpose remove any director from
                office before the expiration of their term of office and may, by
                a majority of votes cast at the meeting, elect any person in
                their stead for the remainder of their term.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                5.09 Validity Of Acts
            </h2>
            <p class="py-2">
                An act of a director or officer is valid notwithstanding an
                irregularity in their election or appointment or a defect in
                their qualification.
            </p>
            <h2 class="text-xl font-bold py-2">
                Meetings Of The Board Of Directors
            </h2>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                6.01 Time and Place Of Meeting
            </h2>
            <p class="py-2">
                Meetings of directors and of any committee of directors may be
                held at any place or by electronic means which permit the
                directors to hear each other. A meeting of directors may be
                convened by the President or any director at any time and the
                Secretary shall upon direction of any of the foregoing convene a
                meeting of directors.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                6.02 Notice
            </h2>
            <p class="py-2">
                Notice of the time and place for the holding of any meeting of
                directors or any committee of directors shall be sent to each
                director not less than four (4) days (exclusive of the day on
                which the notice is sent but inclusive of the day for which
                notice is given) before the date of the meeting; provided that
                the meetings of directors or of any committee of directors may
                be held at any time without notice if all the directors are
                present (except where a director attends a meeting for the
                express purpose of objecting to the transaction of any business
                on the grounds that the meeting is not lawfully called) or if
                all the absent directors have waived notice. The notice of a
                meeting of directors need not specify the purpose or the
                business to be transacted at the meeting.
            </p>
            <p class="py-2">
                For the first meeting of directors to be held following the
                election of directors at an annual or special meeting of the
                members or for a meeting of directors at which a director is
                appointed to fill a vacancy in the Board, no notice of such
                meeting need be given to the newly elected or appointed director
                or directors in order for the meeting to be duly constituted,
                provided a quorum of the directors is present.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                6.03 Waiver Of Notice
            </h2>
            <p class="py-2">
                Notice of any meeting of directors or of any committee of
                directors or the time for the giving of any such notice or any
                irregularity in any meeting or in the notice thereof may be
                waived by any director in writing or by email addressed to the
                Society or in any other manner, and any such waiver may be
                validly given either before or after the meeting to which such
                waiver relates. Attendance of a director at any meeting of
                directors or of any committee of directors is a waiver of notice
                of the meeting, except when a director attends a meeting for the
                express purpose of objecting to the transaction of any business
                on the ground that the meeting is not lawfully called.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                6.04 Omission Of Notice
            </h2>
            <p class="py-2">
                The accidental omission to give notice of any meeting of
                directors or of any committee of directors to or the non-receipt
                of any notice by any person shall not invalidate any resolution
                passed or any proceeding taken at such meeting.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                6.05 Remote Participation
            </h2>
            <p class="py-2">
                A director may participate in a meeting of directors or of any
                committee of directors as a remote participant.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                6.06 Adjournment
            </h2>
            <p class="py-2">
                Any meeting of directors or of any committee of directors may be
                adjourned from time to time by the chair of the meeting, with
                the consent of the meeting, to a fixed time and place. Notice of
                an adjourned meeting of directors or committee of directors is
                not required to be given if the time and place of the adjourned
                meeting is announced at the original meeting. Any adjourned
                meeting shall be duly constituted if held in accordance with the
                terms of the adjournment and a quorum is present thereat. The
                directors who formed a quorum at the original meeting are not
                required to form the quorum at the adjourned meeting. If there
                is no quorum present at the adjourned meeting, the original
                meeting shall be deemed to have terminated forthwith after its
                adjournment. Any business may be brought before or dealt with at
                the original meeting in accordance with the notice calling the
                same.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                6.07 Quorum And Voting
            </h2>
            <p class="py-2">
                One-half (1/2) the number of directors in good standing
                constitutes a quorum at any meeting of directors and,
                notwithstanding any vacancy among the directors, a quorum of
                directors may exercise all the powers of the directors. The
                directors shall not transact business at a meeting of directors
                unless a quorum is present. Questions arising at any meeting of
                directors shall be decided by a majority of votes. In the case
                of an equality of votes, the chair of the meeting in addition to
                their original vote shall have a second or casting vote.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                6.08 Resolution In Lieu Of Meeting
            </h2>
            <p class="py-2">
                A resolution in writing, signed by all the directors entitled to
                vote on that resolution at a meeting of directors or committee
                of directors, is as valid as if it had been passed at a meeting
                of directors or committee of directors. Subject to the Act and
                these Bylaws, the directors may establish by resolution
                mechanisms for conducting electronic votes of the directors or
                for electronically circulating and executing resolutions of the
                directors, and such mechanisms as approved by resolution shall
                be deemed for all purposes to have the same effect as a vote
                taken at a duly-constituted meeting of the directors or a
                resolution executed by signature.
            </p>
            <h2 class="text-xl font-bold py-2">Committees Of Directors</h2>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                7.01 General
            </h2>
            <p class="py-2">
                The directors may from time to time appoint from their members a
                committee of directors, any one or more of their members and may
                delegate to such committee any of the powers of the directors
                except that no such committee shall have the authority to:
            </p>
            <ol class="ml-10" type="a">
                <li class="py-2">
                    (a) submit to the members of the Society any question or
                    matter requiring the approval of the Society;
                </li>
                <li class="py-2">
                    (b) fill a vacancy among the directors or in the office of
                    auditor; and
                </li>
                <li class="py-2">
                    (c) approve any financial statements to be placed before the
                    members of the Society.
                </li>
            </ol>
            <h2 class="text-xl font-bold py-2">
                Remuneration Of Directors, Officers And Employees
            </h2>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                9.01 General
            </h2>
            <p class="py-2">
                No person shall be entitled to any remuneration by reason of
                being a director of the Society provided that the directors may,
                by resolution, award special remuneration to any director in
                undertaking any special services on the Society's behalf other
                than the routine work ordinarily required of a director of the
                Society. The confirmation of any such resolution by the members
                shall not be required. The directors, officers and employees
                shall also be entitled to be paid their travelling and other
                expenses properly and reasonably incurred by them in connection
                with the affairs of the Society.
            </p>
            <h2 class="text-xl font-bold py-2">
                Submission Of Contracts Or Transactions To Members For Approval
            </h2>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                10.01 General
            </h2>
            <p class="py-2">
                The directors in their discretion may submit any contract, act
                or transaction for approval, ratification or confirmation at any
                annual meeting of the members or at any special meeting of the
                members called for the purpose of considering the same and any
                contract, act or transaction that shall be approved, ratified or
                confirmed by resolution passed by a majority of the votes cast
                at any such meeting (unless any different or additional
                requirement is imposed by the Act or by any other by-law) shall
                be as valid and as binding upon the Society and upon all the
                members as though it had been approved, ratified and/or
                confirmed by every member of the Society.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                10.02 Borrowing Power
            </h2>
            <p class="py-2">
                For the purpose of carrying out its objects, the Society may
                borrow or raise or secure the payment of money in such manner as
                it thinks fit, and in particular by the issue of debentures, but
                this power shall be exercised only under the authority of the
                Society, and in no case shall debentures be issued without the
                sanction of a special resolution of the Society.
            </p>
            <h2 class="text-xl font-bold py-2">Conflict Of Interest</h2>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                11.01 General
            </h2>
            <p class="py-2">
                A director or officer of the Society who is a party to a
                material contract or proposed material contract with the
                Society, or is a director or an officer of or has a material
                interest in any person who is a party to a material contract or
                proposed material contract with the Society shall disclose fully
                the nature and extent of their interest. No such director of the
                Society shall vote on any resolution to approve such contract.
                If a material contract is made between the Society and one or
                more of its directors or officers, or between the Society and
                another person of which a director or officer of the Society is
                a director or officer or in which they have a material interest,
                (i) the contract is neither void nor voidable by reason only of
                that relationship, or by reason only that a director with an
                interest in the contract is present at or is counted to
                determine the presence of a quorum at a meeting of directors or
                committee of directors that authorized the contract, and (ii) a
                director or officer or former director or officer of the Society
                to whom a profit accrues as a result of the making of the
                contract is not liable to account to the Society for that profit
                by reason only of holding office as a director or officer, if
                the director or officer disclosed their interest in accordance
                herewith and the contract was approved by the directors or the
                members and it was reasonable and fair to the Society at the
                time it was approved.
            </p>
            <h2 class="text-xl font-bold py-2">
                For The Protection Of Directors And Officers
            </h2>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                12.01 General
            </h2>
            <p class="py-2">
                No director or officer for the time being of the Society shall
                be liable for the acts, receipts, neglects or defaults of any
                other director or officer or employee or for joining in any
                receipt or act for conformity or for any loss, damage or expense
                happening to the Society through the insufficiency or deficiency
                of title to any property acquired by the Society or for or on
                behalf of the Society or for the insufficiency or deficiency of
                any security in or upon which any of the monies of or belonging
                to the Society shall be placed out or invested or for any loss
                or damage arising from the bankruptcy, insolvency or tortious
                act of any person, firm or corporation including any person,
                firm or corporation with whom or which any monies, securities or
                effects shall be lodged or deposited or for any loss,
                conversion, misapplication or misappropriation of or any damage
                resulting from any dealings with any monies, securities or other
                assets belonging to the Society or for any other loss, damage or
                misfortune whatever which may happen in the execution of the
                duties of their respective office of trust or in relation
                thereto, unless the same shall happen by or through their
                failure to exercise the powers and to discharge the duties of
                their office honestly, in good faith with a view to the best
                interests of the Society, and in connection therewith to
                exercise the care, diligence and skill that a reasonably prudent
                person would exercise in comparable circumstances, provided that
                nothing herein contained shall relieve a director or officer
                from the duty to act in accordance with the Act or relieve them
                from liability under the Act. The directors for the time being
                of the Society shall not be under any duty or responsibility in
                respect of any contract, act or transaction whether or not made,
                done or entered into in the name or on behalf of the Society,
                except such as shall have been submitted to and authorized or
                approved by the directors. If any director or officer of the
                Society shall be employed by or shall perform services for the
                Society otherwise than as a director or officer or shall be a
                member of a firm or a member, director or officer of a body
                corporate which is employed by or performs services for the
                Society the fact of their being a member, director or officer of
                the Society or body corporate or member of the firm shall not
                disentitle such director or officer or such firm or body
                corporate, as the case may be, from receiving proper
                remuneration for such services.
            </p>
            <h2 class="text-xl font-bold py-2">Officers Of The Society</h2>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                13.01 Appointment
            </h2>
            <p class="py-2">
                The directors annually or as often as may be required shall
                appoint a President and a Secretary and if deemed advisable may
                appoint one or more Vice-Presidents, a Treasurer and one or more
                Assistant Secretaries and/or one or more Assistant Treasurers.
                Two or more offices of the Society may be held by the same
                person. In case and whenever the same person holds the offices
                of Secretary and Treasurer they may but need not be known as the
                Secretary-Treasurer. The directors may from time to time appoint
                such other officers, employees and agents as they shall deem
                necessary who shall have such authority and shall perform such
                functions and duties as may from time to time be prescribed by
                resolution of the directors. The directors may from time to time
                and subject to the provisions of the Act, vary, add to or limit
                the duties and powers of any officer.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                13.02 Removal Of Officers And Vacation Of Office
            </h2>
            <p class="py-2">
                All officers, employees and agents, in the absence of agreement
                to the contrary, shall be subject to removal by resolution of
                the directors at any time, with or without cause.
            </p>
            <p class="py-2">
                An officer of the Society ceases to hold office when they dies,
                resigns or is removed from office. A resignation of an officer
                becomes effective at the time a written resignation is sent to
                the Society, or at the time specified in the resignation,
                whichever is later.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                13.03 Vacancies
            </h2>
            <p class="py-2">
                If the office of President, Vice-President, Secretary, Assistant
                Secretary, Treasurer, Assistant Treasurer, or any other office
                created by the directors pursuant to paragraph 14.01 hereof
                shall be or become vacant by reason of death, resignation or in
                any other manner whatsoever, the directors shall, in the case of
                the President and Secretary, and may, in the case of any other
                officers, appoint an individual to fill such vacancy.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                13.04 President
            </h2>
            <p class="py-2">
                The President shall be the chief executive officer of the
                Society (except as may otherwise be specified by the Board) and
                shall, subject to the direction of the Board, exercise general
                supervision and control over the business and affairs of the
                Society. If the President is also a director of the Society, the
                President shall, when present, preside as chair at all meetings
                of directors and members. The President shall sign such
                contracts, documents or instruments in writing as require the
                President’s signature and shall have such other powers and shall
                perform such other duties as may from time to time be assigned
                to the President by resolution of the directors or as are
                incident to the President’s office.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                13.05 Vice-President
            </h2>
            <p class="py-2">
                The Vice-President or, if more than one, the Vice-Presidents in
                order of seniority, shall be vested with all the powers and
                shall perform all the duties of the President in the absence or
                inability or refusal to act of the chair, provided, however,
                that a Vice-President who is not a director shall not preside as
                chair at any meeting of directors or members. The Vice-President
                or, if more than one, the Vice-Presidents shall sign such
                contracts, documents or instruments in writing as require the
                Vice-President’s or their signatures and shall also have such
                other powers and shall perform such other duties as may from
                time to time be assigned to the Vice-President or them by
                resolution of the directors.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                13.06 Secretary
            </h2>
            <p class="py-2">
                The Secretary shall give or cause to be given notices for all
                meetings of directors, any committee of directors and members
                when directed to do so and shall, subject to the provisions of
                the Act, maintain the books and records referred to in
                subsections hereof, including keeping an up to date list of all
                members (including honorary members) of the Society. It shall be
                the duty of the Secretary to attend all meetings of directors,
                any committee of directors or members, and to keep accurate
                minutes of the same. The Secretary shall sign such contracts,
                documents or instruments in writing as require the Secretary’s
                signature and shall have such other powers and shall perform
                such other duties as may from time to time be assigned to the
                Secretary by resolution of the directors or as are incident to
                the Secretary’s office.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                13.07 Treasurer
            </h2>
            <p class="py-2">
                Subject to the provisions of any resolution of the directors,
                the Treasurer shall have the care and custody of all the funds
                and securities of the Society and shall deposit the same in the
                name of the Society in such bank or banks or with such other
                depositary or depositaries as the directors may by resolution
                direct. The Treasurer shall prepare and maintain adequate
                accounting records. The Treasurer shall sign such contracts,
                documents or instruments in writing as require the Treasurer’s
                signature and shall have such other powers and shall perform
                such other duties as may from time to time be assigned to the
                Treasurer by resolution of the directors or as are incident to
                the Treasurer’s office. The Treasurer may be required to give
                such bond for the faithful performance of the Treasurer’s duties
                as the directors in their uncontrolled discretion may require
                and no director shall be liable for failure to require any such
                bond or for the insufficiency of any such bond or for any loss
                by reason of the failure of the Society to receive any indemnity
                thereby provided.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                13.08 Assistant Secretary And Assistant Treasurer
            </h2>
            <p class="py-2">
                The Assistant Secretary or, if more than one, the Assistant
                Secretaries in order of seniority, and the Assistant Treasurer
                or, if more than one, the Assistant Treasurers in order of
                seniority, shall be vested with all the powers and shall perform
                all the duties of the Secretary and Treasurer, respectively, in
                the absence or inability or refusal to act of the Secretary or
                Treasurer as the case may be. The Assistant Secretary or, if
                more than one, the Assistant Secretaries and the Assistant
                Treasurer or, if more than one, the Assistant Treasurers shall
                sign such contracts, documents or instruments in writing as
                require the Assistant Secretary’s or their signatures
                respectively and shall have such other powers and shall perform
                such other duties as may from time to time be assigned to the
                Assistant Secretary or them by resolution of the directors.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                13.09 Duties Of Officers May Be Delegated
            </h2>
            <p class="py-2">
                In case of the absence or inability or refusal to act of any
                officer of the Society or for any other reason that the
                directors may deem sufficient, the directors may delegate all or
                any of the powers of such officer to any other officer or to any
                director for the time being.
            </p>
            <h2 class="text-xl font-bold py-2">Auditors</h2>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                14.01 General
            </h2>
            <p class="py-2">
                The financial statements of the Society shall be audited once a
                year by a recognized firm of Chartered Accountants or by two
                members of the society who shall be appointed from time to time
                by the Board at such remuneration as may be fixed by the Board.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                14.02 Inspection Of Records
            </h2>
            <p class="py-2">
                The books and records of the Society may be inspected by any
                member of the Society at the annual meeting provided for herein
                or at any time upon giving reasonable notice and arranging a
                time satisfactory to the officer or officers having charge of
                the same. Each of the directors shall at all times have access
                to such books and records.
            </p>
            <h2 class="text-xl font-bold py-2">Cheques, Drafts, Notes, Etc.</h2>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                15.01 General
            </h2>
            <p class="py-2">
                All cheques, drafts or orders for the payment of money and all
                notes, acceptances and bills of exchange shall be signed by any
                officer of the Society or any other individual as the directors
                may from time to time designate by resolution.
            </p>
            <h2 class="text-xl font-bold py-2">Execution Of Contracts, Etc.</h2>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                16.01 General
            </h2>
            <p class="py-2">
                All contracts, engagements, applications, deeds, documents and
                other instruments in writing of the Society of whatsoever kind
                or nature shall be executed by any two (2) of the directors or
                officers of the Society and all contracts, documents or
                instruments in writing so signed shall be binding upon the
                Society without any further authorization or formality. The
                directors are authorized from time to time by resolution to
                appoint any officer or officers or any other person or persons
                on behalf of the Society either to sign contracts, documents or
                instruments in writing generally or to sign specific contracts,
                documents or instruments in writing.
            </p>
            <p class="py-2">
                The corporate seal of the Society may, when required, be affixed
                by the President to contracts, documents or instruments in
                writing signed by them as aforesaid or by an officer or
                officers, person or person appointed as aforesaid by resolution
                of the Board.
            </p>
            <p class="py-2">
                The term "contracts, documents or instruments in writing" as
                used in this by-law shall include deeds, mortgages, hypothecs,
                charges, conveyances, transfers and assignments of property,
                real or personal, immovable or movable, agreements, releases,
                receipts and discharges for the payment of money or other
                obligations, conveyances, transfers and assignments of
                securities and all paper writings.
            </p>
            <p class="py-2">
                In particular, without limiting the generality of the foregoing,
                the President alone is authorized to sell, assign, transfer,
                exchange, convert or convey all securities owned by or
                registered in the name of the Society and to sign and execute
                (under the seal of the Society or otherwise) all assignments,
                transfers, conveyances, powers of attorney and other instruments
                that may be necessary for the purpose of selling, assigning,
                transferring, exchanging, converting or conveying any such
                securities.
            </p>
