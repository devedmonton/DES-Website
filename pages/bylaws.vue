                    chair or on the question of adjournment or termination, the
                    ballot shall be taken forthwith without adjournment. If a
                    ballot is demanded on any other question or as to the
                    election of directors, the ballot shall be taken in such
                    manner and either at once or later at the meeting or after
                    adjournment as the chair of the meeting directs. The result
                    of a ballot shall be deemed to be the resolution of the
                    meeting at which the ballot was demanded. A demand for a
                    ballot may be withdrawn.
                </p>
