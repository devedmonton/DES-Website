            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                17.01 General
            </h2>
            <p class="py-2">
                No amendment of or addition to the Bylaws shall be made except
                by a special resolution of the members of the Society in
                accordance with the Act at a meeting of the members of the
                Society called for that purpose or by special resolution in
                accordance with the Act.
            </p>
            <h2 class="text-xl font-bold py-2">Distribution Of Property</h2>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                18.01 General
            </h2>
            <p class="py-2">
                Upon the dissolution of the Society and after the payment of all
                debts and liabilities, the remaining property of the Society
                shall be distributed or disposed of to charitable organizations
                or to organizations the objects of which are beneficial to the
                community.
            </p>
