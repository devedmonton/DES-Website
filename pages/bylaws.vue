                19.04 Computation Of Time
            </h2>
            <p class="py-2">
                Where a given number of days' notice or notice extending over
                any period is required to be given under any provisions of the
                bylaws of the Society, the day the notice is sent shall, unless
                it is otherwise provided, be counted in such number of days or
                other period and such notice shall be deemed to have been sent
                on the day of personal delivery or mailing.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                19.05 Proof Of Service
            </h2>
            <p class="py-2">
                A certificate of any officer of the Society in office at the
                time of the making of the certificate or of an agent of the
                Society as to facts in relation to the sending of any notice or
                document to any member, director, officer or auditor or
                publication of any notice or document shall be conclusive
                evidence thereof and shall be binding on every member, director,
                officer or auditor of the Society, as the case may be.
            </p>
