                    members of the Society may participate in the meeting as a
                    remote participant.
                </p>
                <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                    4.12 Adjournment
                </h2>
                <p class="py-2">
                    The chair of the meeting may with the consent of the meeting
                    adjourn any meeting of members of the Society from time to
                    time to a fixed time and place and if the meeting is
                    adjourned by one or more adjournments for an aggregate of
                    less than thirty (30) days it is not necessary to give
                    notice of the adjourned meeting other than by announcement
                    at the time of an adjournment. If a meeting of members is
                    adjourned by one or more adjournments for an aggregate of
                    thirty (30) days or more, notice of the adjourned meeting
                    shall be given as for an original meeting.
                </p>
                <p class="py-2">
                    Any adjourned meeting shall be duly constituted if held in
                    accordance with the terms of the adjournment and a quorum is
                    present thereat. The persons who formed a quorum at the
                    original meeting are not required to form the quorum at the
                    adjourned meeting. If there is no quorum present at the
                    adjourned meeting, the original meeting shall be deemed to
                    have terminated forthwith after its adjournment. Any
                    business may be brought before or dealt with at any
                    adjourned meeting which might have been brought before or
                    dealt with at the original meeting in accordance with the
                    notice calling the same.
                </p>
                <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                    4.13 Quorum
                </h2>
                <p class="py-2">
                    A quorum for the transaction of business at any meeting of
                    members shall consist of five (5) members in good standing.
                    If a quorum is present at the opening of a meeting of
                    members the members may proceed with the business of the
                    meeting notwithstanding that a quorum is not present
                    throughout the meeting.
                </p>
