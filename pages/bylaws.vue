                    4.09 Chair Of The Meeting
                </h2>
                <p class="py-2">
                    Each member may vote by proxy. Such proxy must themself be a
                    member of the Society, but before voting must produce and
                    deposit with the Secretary a sufficient appointment in
                    writing. The Secretary or in their absence the chair of the
                    meeting shall have complete discretion to determine whether
                    an appointment or proxy is valid and sufficient.
                </p>
                <p class="py-2">
