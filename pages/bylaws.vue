            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                19.01 General
            </h2>
            <p class="py-2">
                Any notice or document required by the Act or the bylaws to be
                sent to any member or director of the Society may be delivered
                personally to or sent by email addressed to:
            </p>
            <ol class="ml-10" type="a">
                <li class="py-2">
                    (a) the member at their latest email address as shown in the
                    records of the Society; and
                </li>
                <li class="py-2">
                    (b) the director at their latest email address as shown in
                    the records of the Society,
                </li>
            </ol>
            <p class="py-2">
                With respect to every notice or document sent by email it shall
                be sufficient to prove that the message containing the notice or
                document was properly addressed and sent to the email server for
                distribution.
            </p>
            <p class="py-2">
                <b>19.02</b> Service of any notice required under the Act or
                Bylaws shall be deemed sufficiently served when sent to the
                email address provided by the member at the time of becoming a
                member, or such other email address as provided to the Secretary
                by such member from time to time. Any reference in these Bylaws
                to “in writing” shall be deemed to include correspondence sent
                and received by electronic means.
            </p>
            <h2 class="text-xl font-bold py-2 border-b border-lightgrey">
                19.03 Signatures To Notices
            </h2>
            <p class="py-2">
                The signature of any director or officer of the Society to any
                notice may be written, stamped, typewritten, electronically
                affixed or printed or partly written, stamped, typewritten,
                electronically affixed or printed.
            </p>
