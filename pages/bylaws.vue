                    4.14 Resolution In Lieu Of Meeting
                </h2>
                <p class="py-2">
                    A resolution in writing signed by all the members entitled
                    to vote on that resolution is as valid as if it had been
                    passed at a meeting of the members of the Society.
                </p>
